public class ElevarNumero {
    public static void main(String[] args) {
        ElevaNumero(3,4);
        ElevaNumero(3,5);
        ElevaNumero(3,6);
        ElevaNumero(3,7);
        ElevaNumero(3,8);
        System.out.println("Hola Mundo");
    }


    public static void ElevaNumero(int base, int exponente){
        
        System.out.println(Math.pow(base, exponente));
    }
}
